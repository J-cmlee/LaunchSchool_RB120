class PokerHand
  attr_accessor :cards
  def initialize(deck)
    @cards = []
    5.times { cards << deck.draw }
  end

  def print
    cards.each { |card| puts card }
  end

  def evaluate
    if royal_flush?     then 'Royal flush'
    elsif straight_flush?  then 'Straight flush'
    elsif four_of_a_kind?  then 'Four of a kind'
    elsif full_house?      then 'Full house'
    elsif flush?           then 'Flush'
    elsif straight?        then 'Straight'
    elsif three_of_a_kind? then 'Three of a kind'
    elsif two_pair?        then 'Two pair'
    elsif pair?            then 'Pair'
    else 'High card'
    end
  end

  private

  def royal_flush?
    flush? && @cards.min.rank == 10
  end

  def straight_flush?
    straight? && flush?
  end

  def four_of_a_kind?
    value_count.include?(4)
  end

  def full_house?
    counter = value_count
    counter.include?(3) && counter.include?(2)
  end

  def flush?
    suit_values.uniq.length == 1
  end

  def straight?
    sorted = hand_values.sort
    if hand_values.include?(14)
      return true if sorted == [2, 3, 4, 5, 14]
    end
    sorted == (sorted[0]..sorted[-1]).to_a
  end

  def three_of_a_kind?
    value_count.include?(3)
  end

  def two_pair?
    sorted = value_count.sort
    sorted[-1] == 2 &&
      sorted[-2] == 2
  end

  def pair?
    value_count.include?(2)
  end

  def hand_values
    cards.map(&:value)
  end

  def suit_values
    cards.map(&:suit)
  end

  def value_count
    hand_values.each_with_object(Hash.new(0)) { |e, total| total[e] += 1; }.values
  end
end

